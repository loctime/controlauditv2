// ‚úÖ Funci√≥n espec√≠fica para manejar redirect en APK
export const handleAPKGoogleRedirect = async () => {
  try {
    console.log('üì± Manejando redirect de Google en APK...');
    
    // ‚úÖ Verificar si estamos en APK
    if (!isAPK()) {
      console.log('‚ùå No estamos en APK, saltando manejo de redirect');
      return null;
    }
    
    // ‚úÖ Verificar resultado del redirect
    const result = await checkGoogleRedirectResult();
    
    if (result && result.user) {
      console.log('‚úÖ Usuario autenticado en APK:', result.user.uid);
      
      // ‚úÖ Configurar listener para cambios de estado de la app
      if (window.Capacitor && window.Capacitor.App) {
        console.log('üì± Configurando listener de estado de app para APK...');
        
        window.Capacitor.App.addListener('appStateChange', ({ isActive }) => {
          console.log('üì± Estado de app cambiado:', isActive ? 'Activa' : 'Inactiva');
          
          if (isActive) {
            // ‚úÖ Cuando la app vuelve a estar activa, verificar el redirect
            console.log('üì± App activa, verificando redirect...');
            checkGoogleRedirectResult().then(redirectResult => {
              if (redirectResult && redirectResult.user) {
                console.log('‚úÖ Redirect procesado despu√©s de activar app:', redirectResult.user.uid);
                // Aqu√≠ podr√≠as emitir un evento o callback para manejar el login
              }
            });
          }
        });
        
        window.Capacitor.App.addListener('appUrlOpen', (data) => {
          console.log('üì± URL abierta en app:', data.url);
          // ‚úÖ Manejar URL de retorno de Google OAuth
          if (data.url && data.url.includes('__/auth/handler')) {
            console.log('üì± URL de auth handler detectada, procesando...');
            checkGoogleRedirectResult().then(redirectResult => {
              if (redirectResult && redirectResult.user) {
                console.log('‚úÖ Redirect procesado desde URL:', redirectResult.user.uid);
                // Aqu√≠ podr√≠as emitir un evento o callback para manejar el login
              }
            });
          }
        });
      }
      
      return result;
    }
    
    return null;
    
  } catch (error) {
    console.error('‚ùå Error manejando redirect de Google en APK:', error);
    return null;
  }
};

// ‚úÖ Funci√≥n para configurar listener de app state en APK
let appStateListener = null;
let urlChangeListener = null;

const setupAppStateListener = async () => {
  try {
    // Solo configurar si estamos en APK usando la funci√≥n robusta
    if (!isAPK()) {
      return;
    }
    
    // Importar din√°micamente para evitar errores en web
    const { App } = await import('@capacitor/app');
    
    if (appStateListener) {
      appStateListener.remove();
    }
    
    appStateListener = App.addListener('appStateChange', async ({ isActive }) => {
      console.log('üì± App state changed:', { isActive });
      
      if (isActive) {
        // App volvi√≥ al primer plano, verificar si hay resultado de redirect
        console.log('üì± App volvi√≥ al primer plano, verificando redirect...');
        
        try {
          const result = await getRedirectResult(auth);
          if (result) {
            console.log('‚úÖ Redirect procesado exitosamente en APK:', result);
            // El onAuthStateChanged se encargar√° del resto
          }
        } catch (error) {
          console.error('‚ùå Error procesando redirect en APK:', error);
        }
      }
    });
    
    // ‚úÖ Tambi√©n configurar listener de cambios de URL
    if (urlChangeListener) {
      urlChangeListener.remove();
    }
    
    urlChangeListener = App.addListener('appUrlOpen', async (data) => {
      console.log('üì± App URL opened:', data);
      
      // Si la URL contiene el handler de Firebase, procesar el redirect
      if (data.url && data.url.includes('__/auth/handler')) {
        console.log('üì± Firebase auth handler detectado, procesando redirect...');
        
        try {
          const result = await getRedirectResult(auth);
          if (result) {
            console.log('‚úÖ Redirect procesado exitosamente en APK:', result);
            // El onAuthStateChanged se encargar√° del resto
          }
        } catch (error) {
          console.error('‚ùå Error procesando redirect en APK:', error);
        }
      }
    });
    
    console.log('üì± Listeners de app state y URL configurados para APK');
  } catch (error) {
    console.error('‚ùå Error configurando listeners de APK:', error);
  }
};

// ‚úÖ Funci√≥n para limpiar listeners
export const cleanupAppStateListener = () => {
  if (appStateListener) {
    try {
      appStateListener.remove();
      appStateListener = null;
      console.log('üì± Listener de app state limpiado');
    } catch (error) {
      console.error('‚ùå Error limpiando listener de app state:', error);
    }
  }
  
  if (urlChangeListener) {
    try {
      urlChangeListener.remove();
      urlChangeListener = null;
      console.log('üì± Listener de URL limpiado');
    } catch (error) {
      console.error('‚ùå Error limpiando listener de URL:', error);
    }
  }
};

