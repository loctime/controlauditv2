// ‚úÖ Funci√≥n SIMPLE y NUEVA para Google Auth (evitar errores)
export const signInWithGoogleSimple = async () => {
  console.log('üöÄ signInWithGoogleSimple iniciada...');
  
  try {
    // ‚úÖ Verificar que Firebase est√© disponible
    if (!auth) {
      throw new Error('Firebase Auth no est√° disponible');
    }
    
    // ‚úÖ Detectar plataforma
    const isAPKPlatform = isAPK();
    console.log('üì± ¬øEs APK?', isAPKPlatform);
    
    if (isAPKPlatform) {
      console.log('üì± APK detectado, usando Google Auth nativo...');
      
      // ‚úÖ Para APK: usar el plugin nativo de Capacitor
      try {
        // Importar din√°micamente para evitar errores en web
        const { GoogleAuth } = await import('@southdevs/capacitor-google-auth');
        
        // ‚úÖ Importar configuraci√≥n de APK para obtener el Web Client ID correcto
        const { FIREBASE_APK_CONFIG } = await import('./config/firebaseAPK');
        const webClientId = FIREBASE_APK_CONFIG.oauth.webClientId;
        
        console.log('üì± Web Client ID configurado:', webClientId);
        
        // Inicializar Google Auth
        await GoogleAuth.initialize({
          clientId: webClientId,
          scopes: ['email', 'profile']
        });
        
        // Iniciar sesi√≥n nativa
        const result = await GoogleAuth.signIn();
        console.log('üì± Resultado de Google Auth nativo:', result);
        
        if (result?.authentication?.idToken) {
          // ‚úÖ Crear credencial de Firebase con el idToken
          const { GoogleAuthProvider, signInWithCredential } = await import('firebase/auth');
          const credential = GoogleAuthProvider.credential(result.authentication.idToken);
          
          // ‚úÖ Iniciar sesi√≥n en Firebase
          const firebaseResult = await signInWithCredential(auth, credential);
          console.log('‚úÖ Usuario autenticado en Firebase:', firebaseResult);
          
          return { 
            success: true, 
            user: firebaseResult.user,
            pendingRedirect: false 
          };
        } else {
          throw new Error('No se obtuvo idToken de Google');
        }
        
      } catch (error) {
        console.error('‚ùå Error con Google Auth nativo:', error);
        
        // ‚úÖ Fallback: mostrar error espec√≠fico
        if (error.message.includes('DEVELOPER_ERROR')) {
          throw new Error('Error de configuraci√≥n de Google OAuth. Verifica el Client ID y SHA-1 en Firebase Console.');
        } else if (error.message.includes('Sign in failed')) {
          throw new Error('Error al iniciar sesi√≥n con Google. Verifica tu conexi√≥n a internet.');
        } else {
          throw new Error(`Error de autenticaci√≥n: ${error.message}`);
        }
      }
      
    } else {
      console.log('üåê Web detectado, usando popup...');
      
      // ‚úÖ Para Web: usar popup con Firebase
      const { GoogleAuthProvider, signInWithPopup } = await import('firebase/auth');
      const provider = new GoogleAuthProvider();
      provider.addScope('email');
      provider.addScope('profile');
      
      const result = await signInWithPopup(auth, provider);
      console.log('‚úÖ Google Auth exitoso (popup):', result);
      
      return { 
        success: true, 
        user: result.user,
        pendingRedirect: false 
      };
    }
    
  } catch (error) {
    console.error('‚ùå Error en signInWithGoogleSimple:', error);
    
    // ‚úÖ Manejar errores espec√≠ficos
    let errorMessage = 'Error al iniciar sesi√≥n con Google';
    
    if (error.message.includes('DEVELOPER_ERROR')) {
      errorMessage = 'Error de configuraci√≥n de Google OAuth. Verifica el Client ID y SHA-1 en Firebase Console.';
    } else if (error.message.includes('Sign in failed')) {
      errorMessage = 'Error al iniciar sesi√≥n con Google. Verifica tu conexi√≥n a internet.';
    } else if (error.message.includes('popup-closed-by-user')) {
      errorMessage = 'Ventana cerrada por el usuario';
    } else if (error.message.includes('popup-blocked')) {
      errorMessage = 'Popup bloqueado por el navegador';
    } else if (error.message.includes('unauthorized-domain')) {
      errorMessage = 'Dominio no autorizado para Google OAuth';
    } else if (error.message) {
      errorMessage = error.message;
    }
    
    // ‚úÖ Mostrar error con toast si est√° disponible
    if (typeof toast !== 'undefined') {
      toast.error(errorMessage, {
        position: "top-left",
        autoClose: 5000,
      });
    }
    
    throw new Error(errorMessage);
  }
};

